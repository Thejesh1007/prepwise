import { useEffect, useState } from 'react'
import { useParams, useNavigate, Link } from 'react-router-dom'
import api from '../api/axios.js'

export default function Result() {
  const { id } = useParams()
  const navigate = useNavigate()
  const [content, setContent] = useState(null)
  const [loading, setLoading] = useState(true)
  const [error, setError] = useState('')
  const [copied, setCopied] = useState(false)

  useEffect(() => {
    const loadResult = async () => {
      try {
        const res = await api.get(`/api/history/${id}`)
        setContent(res.data.content)
      } catch {
        setError('Failed to load this result.')
      } finally {
        setLoading(false)
      }
    }
    loadResult()
  }, [id])

  const handleCopy = () => {
    navigator.clipboard.writeText(content.output).then(() => {
      setCopied(true)
      setTimeout(() => setCopied(false), 2000)
    })
  }

  const handleExportPDF = () => {
    const printWindow = window.open('', '_blank')
    printWindow.document.write(`
      <!DOCTYPE html>
      <html>
        <head>
          <title>${content.topic} — PrepWise</title>
          <style>
            * { box-sizing: border-box; margin: 0; padding: 0; }
            body {
              font-family: 'Georgia', serif;
              font-size: 14px;
              line-height: 1.8;
              color: #1a1a1a;
              padding: 48px 56px;
              max-width: 800px;
              margin: 0 auto;
            }
            .header {
              border-bottom: 2px solid #7c3aed;
              padding-bottom: 16px;
              margin-bottom: 28px;
            }
            .badge {
              display: inline-block;
              background: #ede9fe;
              color: #7c3aed;
              font-size: 10px;
              font-weight: 700;
              letter-spacing: 0.06em;
              text-transform: uppercase;
              padding: 2px 10px;
              border-radius: 20px;
              margin-bottom: 10px;
              font-family: 'Arial', sans-serif;
            }
            .badge-objective { background: #dcfce7; color: #16a34a; }
            .badge-seminar { background: #fef3c7; color: #d97706; }
            h1 {
              font-size: 22px;
              font-weight: 700;
              margin-bottom: 4px;
              color: #111;
            }
            .meta {
              font-size: 12px;
              color: #666;
              font-family: 'Arial', sans-serif;
              margin-bottom: 6px;
            }
            .options {
              display: flex;
              flex-wrap: wrap;
              gap: 8px;
              margin-top: 10px;
            }
            .chip {
              background: #f4f4f5;
              border: 1px solid #e4e4e7;
              border-radius: 20px;
              padding: 2px 10px;
              font-size: 11px;
              color: #555;
              font-family: 'Arial', sans-serif;
            }
            .output {
              white-space: pre-wrap;
              word-break: break-word;
              line-height: 1.85;
              font-size: 14px;
              color: #1a1a1a;
            }
            .footer {
              margin-top: 40px;
              padding-top: 16px;
              border-top: 1px solid #e4e4e7;
              font-size: 11px;
              color: #999;
              font-family: 'Arial', sans-serif;
              text-align: center;
            }
            @media print {
              body { padding: 32px 40px; }
            }
          </style>
        </head>
        <body>
          <div class="header">
            <div class="badge badge-${content.mode.toLowerCase()}">${content.mode}</div>
            <h1>${content.topic}</h1>
            <p class="meta">${content.subject} &middot; ${new Date(content.createdAt).toLocaleDateString('en-IN', { day: 'numeric', month: 'long', year: 'numeric' })}</p>
            <div class="options">
              ${Object.entries(content.options).map(([k, v]) => `<span class="chip">${k}: ${v}</span>`).join('')}
            </div>
          </div>
          <pre class="output">${content.output.replace(/</g, '&lt;').replace(/>/g, '&gt;')}</pre>
          <div class="footer">Generated by PrepWise &mdash; AI Study Assistant</div>
        </body>
      </html>
    `)
    printWindow.document.close()
    printWindow.focus()
    setTimeout(() => {
      printWindow.print()
      printWindow.close()
    }, 500)
  }

  if (loading) return (
    <div style={{
      display: 'flex', alignItems: 'center', justifyContent: 'center',
      minHeight: 'calc(100vh - 56px)', gap: '10px',
      color: 'var(--text-2)', fontSize: '14px',
    }}>
      <span className="spinner" style={{ borderTopColor: 'var(--accent-light)' }} />
      Loading result...
    </div>
  )

  if (error) return (
    <div style={{ maxWidth: '780px', margin: '0 auto', padding: '40px 24px' }}>
      <div className="error-box" style={{ marginBottom: '16px' }}>{error}</div>
      <button className="btn btn-ghost" onClick={() => navigate('/history')}>← Back to History</button>
    </div>
  )

  return (
    <div style={{ maxWidth: '780px', margin: '0 auto', padding: '40px 24px', width: '100%' }}>

      {/* Header row */}
      <div style={{
        display: 'flex', alignItems: 'flex-start', justifyContent: 'space-between',
        gap: '16px', marginBottom: '20px', flexWrap: 'wrap',
      }}>
        <div>
          <div style={{ marginBottom: '8px' }}>
            <span className={`badge badge-${content.mode.toLowerCase()}`}>{content.mode}</span>
          </div>
          <h1 style={{ fontSize: '20px', fontWeight: '700', letterSpacing: '-0.02em', marginBottom: '4px' }}>
            {content.topic}
          </h1>
          <p style={{ fontSize: '13px', color: 'var(--text-2)' }}>
            {content.subject} &middot;{' '}
            {new Date(content.createdAt).toLocaleDateString('en-IN', {
              day: 'numeric', month: 'short', year: 'numeric',
            })}
          </p>
        </div>

        {/* Action buttons */}
        <div style={{ display: 'flex', gap: '8px', flexWrap: 'wrap' }}>
          <button
            className="btn btn-ghost btn-sm"
            onClick={handleCopy}
            style={{ minWidth: '90px' }}
          >
            {copied ? '✓ Copied' : 'Copy'}
          </button>
          <button
            className="btn btn-ghost btn-sm"
            onClick={handleExportPDF}
          >
            Export PDF
          </button>
          <Link to="/generate" className="btn btn-primary btn-sm">+ New</Link>
          <Link to="/history" className="btn btn-ghost btn-sm">History</Link>
        </div>
      </div>

      {/* Options chips */}
      <div style={{ display: 'flex', gap: '6px', flexWrap: 'wrap', marginBottom: '20px' }}>
        {Object.entries(content.options).map(([k, v]) => (
          <span key={k} style={{
            background: 'var(--surface)', border: '1px solid var(--border)',
            borderRadius: '20px', padding: '3px 11px',
            fontSize: '12px', color: 'var(--text-2)',
          }}>
            {k}: <span style={{ color: 'var(--text-1)', fontWeight: '500' }}>{String(v)}</span>
          </span>
        ))}
      </div>

      {/* Output */}
      <div className="card" style={{ padding: '28px 32px' }}>
        <pre style={{
          whiteSpace: 'pre-wrap',
          wordBreak: 'break-word',
          fontSize: '14px',
          lineHeight: '1.85',
          color: 'var(--text-1)',
          fontFamily: "'DM Sans', sans-serif",
          margin: 0,
        }}>
          {content.output}
        </pre>
      </div>
    </div>
  )
}